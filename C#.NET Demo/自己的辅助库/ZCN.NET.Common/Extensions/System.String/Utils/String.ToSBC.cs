namespace ZCN.NET.Common.Extensions
{
    public static partial class CommonExtensions
    {
        #region 半角全角转换

        /// <summary>
        /// 自定义扩展方法：转全角的函数(SBC case)
        /// </summary>
        /// <param name="this">扩展对象,任意字符串</param>
        /// <returns>全角字符串</returns>
        ///<remarks>
        ///全角空格为12288，半角空格为32
        ///其他字符半角(33-126)与全角(65281-65374)的对应关系是：均相差65248
        ///</remarks>
        public static string ConvertToToSBC(this string @this)
        {
            //半角转全角：
            char[] c = @this.ToCharArray();
            for(int i = 0;i < c.Length;i++)
            {
                if(c[i] == 32)
                {
                    c[i] = (char)12288;
                    continue;
                }
                if(c[i] < 127)
                {
                    c[i] = (char)(c[i] + 65248);
                }
            }
            return new string(c);
        }

        /// <summary>
        /// 自定义扩展方法：转半角的函数(SBC case)
        /// </summary>
        /// <param name="this">扩展对象,任意字符串</param>
        /// <returns>全角字符串</returns>
        ///<remarks>
        ///全角空格为12288，半角空格为32
        ///其他字符半角(33-126)与全角(65281-65374)的对应关系是：均相差65248
        ///</remarks>
        public static string ConvertToDBC(string @this)
        {
            char[] c = @this.ToCharArray();
            for(int i = 0;i < c.Length;i++)
            {
                if(c[i] == 12288)
                {
                    c[i] = (char)32;
                    continue;
                }
                if(c[i] > 65280 && c[i] < 65375)
                    c[i] = (char)(c[i] - 65248);
            }
            return new string(c);
        }

        #endregion
    }
}