namespace ZCN.NET.CommonX.Constants
{
    /// <summary>
    ///  .NET平台下的系统数据类型名称
    /// </summary>
    public class DotNetSystemType
    {
        /// <summary>
        /// System.Boolean，对应 C# 为 bool
        /// </summary>
        public const string SYSTEM_BOOLEAN = "System.Boolean";

        /// <summary>
        /// System.Byte，对应 C# 为 byte
        /// </summary>
        public const string SYSTEM_BYTE = "System.Byte";

        /// <summary>
        /// System.SByte，对应 C# 为 sbyte
        /// </summary>
        public const string SYSTEM_SBYTE = "System.SByte";

        /// <summary>
        /// System.Char，对应 C# 为 char
        /// </summary>
        public const string SYSTEM_CHAR = "System.Char";

        /// <summary>
        /// System.Decimal，对应 C# 为 decimal
        /// </summary>
        public const string SYSTEM_DECIMAL = "System.Decimal";


        /// <summary>
        /// System.Double，对应 C# 为 double
        /// </summary>
        public const string SYSTEM_DOUBLE = "System.Double";

        /// <summary>
        /// System.Single，对应 C# 为 float
        /// </summary>
        public const string SYSTEM_SINGLE = "System.Single";

        /// <summary>
        /// System.Int16，对应 C# 为 short
        /// </summary>
        public const string SYSTEM_INT16 = "System.Int16";

        /// <summary>
        /// System.UInt16，对应 C# 为 uint16
        /// </summary>
        public const string SYSTEM_U_INT16 = "System.UInt16";

        /// <summary>
        /// System.Int32，对应 C# 为 int
        /// </summary>
        public const string SYSTEM_INT32 = "System.Int32";

        /// <summary>
        /// System.UInt32，对应 C# 为 uint32
        /// </summary>
        public const string SYSTEM_U_INT32 = "System.UInt32";

        /// <summary>
        /// System.Int64，对应 C# 为 long
        /// </summary>
        public const string SYSTEM_INT64 = "System.Int64";

        /// <summary>
        /// System.UInt64，对应 C# 为 ulong
        /// </summary>
        public const string SYSTEM_U_INT64 = "System.UInt64";

        /// <summary>
        /// System.Object，对应 C# 为 object
        /// </summary>
        public const string SYSTEM_OBJECT = "System.Object";

        /// <summary>
        /// System.String，对应 C# 为 string
        /// </summary>
        public const string SYSTEM_STRING = "System.String";
    }
}